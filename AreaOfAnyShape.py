width = int(input('Enter width for rectangle : '))
heightForTriangle = int(input('Enter height for rectangle: '))
areaOfRectangle = width * heightForTriangle
print('Area of rectangle :', areaOfRectangle)

radius = float(input('radius for circle'))
pi = 3.14159
areaOfCircle = pi * (radius ** 2)
print('Area of Circle :', areaOfCircle)

heightForTriangle = int(input('Enter height for triangle :'))
base = int(input('Enter base for triangle :'))
areaOfTriangle = 1 / 2 * heightForTriangle * base
print('Area of triangle: ', areaOfTriangle)
